def sum_three_numbers(a, b, c):
    """This function returns the sum of three numbers."""
    return a + b + c

if __name__ == "__main__":
    # Example numbers
    num1 = 10
    num2 = 20
    num3 = 30

    # Calculate the sum
    total = sum_three_numbers(num1, num2, num3)

    # Print the result
    print(f"The sum of {num1}, {num2}, and {num3} is: {total}")

